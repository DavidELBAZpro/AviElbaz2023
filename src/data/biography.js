// Influencé et guidé par son père mélomane, grand-interprète et adepte des musiques du monde, il berce très tôt dans cet univers pour lequel il voue une admiration sans pareille qui suscite en lui naturellement l'envie d'interpréter à l'oreille les morceaux qui lui plaisent.  
